#  tuples A tuple is an immutable list i.e a list that cannot be changed. It can only be rewritten

names = ("Adeyefa", "Oluwatoba", "Moronfooluwa", "Akinkuni")

for name in names:
    print(name)

names = ("Oluwa", "Samuel", "Israel")  # rewritting the names tuple

for name in names:
    print(name)
